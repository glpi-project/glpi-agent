sub getCountryMCC {
    my ($mcc) = @_;

    return unless $mcc;

    return $MobileCountryCodes{$mcc} if $MobileCountryCodes{$mcc};

    return unless ($mcc =~ /^[128]\d\d$/);

    return 'Reserved';
}

